#include <iostream>
using namespace std;

int main()
{
    // Integer Type
    int i = 10;
    cout << "Value of variable i is " << i << endl;
    cout << "Size of datatype int is " << sizeof(i) << endl;
    cout << "******************************" << endl;
    // Short Integer Type
    short int s = 25;
    cout << "Value of variable s is " << s << endl;
    cout << "Size of datatype short int is " << sizeof(s) << endl;
    cout << "******************************" << endl;
    // Long Integer Type
    long long int l = 30;
    cout << "Value of variable l is " << l << endl;
    cout << "Size of datatype long long int is " << sizeof(l) << endl;
    cout << "******************************" << endl;
    // Float Type
    float f = 1.23;
    cout << "Value of variable f is " << f << endl;
    cout << "Size of datatype float is " << sizeof(f) << endl;
    cout << "******************************" << endl; 
    // Double Type
    double d = 3.275;
    cout << "Value of variable d is " << d << endl;
    cout << "Size of datatype double is " << sizeof(d) << endl;
    cout << "******************************" << endl;
    // Char Type
    char c = 'a';
    cout << "Value of variable c is " << c << endl;
    cout << "Size of datatype char is " << sizeof(c) << endl;
    cout << "******************************" << endl;
    // Bool Type
    bool b = true;
    cout << "Value of variable b is " << b << endl;
    cout << "Size of datatype b is " << sizeof(b) << endl;
    cout << "******************************" << endl;
    int j;
    cout << j << endl;
    
    float g;
    cout << g << endl;

    bool b2;
    cout << b2 << endl;

    char c2;
    cout << c2 << endl;

    return 0;
}